global max
global power
global gcd

;*******************************************************************************
;* int max(int x, int y);                                                      *
;*******************************************************************************
%define y [ebp+12]
%define x [ebp+8]
max:
  push  ebp
  mov   ebp, esp

  mov   eax, x                     ; if (x > y)
  cmp   eax, y                     ;   return x;
  jg    .else                      ; else
  mov   eax, y                     ;   return y;

.else:
  mov   esp, ebp
  pop   ebp
  ret

;*******************************************************************************
;* int power(int x, int n);                                                    *
;*******************************************************************************
power:
  ret

;*******************************************************************************
;* int gcd(int a, int b);                                                      *
;*******************************************************************************
gcd:
  ret
